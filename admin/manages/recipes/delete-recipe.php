<?php
include '../../includes/auth_check.php';
include '../../includes/db_config.php';

/*
|--------------------------------------------------------------------------
| CHECK ID
|--------------------------------------------------------------------------
*/

if(!isset($_GET['id']))
{
    header("Location: recipes.php");
    exit();
}

$id = (int)$_GET['id'];

/*
|--------------------------------------------------------------------------
| FETCH RECIPE
|--------------------------------------------------------------------------
*/

$stmt = $pdo->prepare("SELECT * FROM recipes WHERE id = ?");
$stmt->execute([$id]);

$recipe = $stmt->fetch();

/*
|--------------------------------------------------------------------------
| RECIPE NOT FOUND
|--------------------------------------------------------------------------
*/

if(!$recipe)
{
    header("Location: recipes.php");
    exit();
}

/*
|--------------------------------------------------------------------------
| DELETE IMAGE
|--------------------------------------------------------------------------
*/

$imagePath = "./uploads/recipes/" . $recipe['recipe_image'];

if(!empty($recipe['recipe_image']) && file_exists($imagePath))
{
    unlink($imagePath);
}

/*
|--------------------------------------------------------------------------
| DELETE RECIPE
|--------------------------------------------------------------------------
*/

$deleteStmt = $pdo->prepare("DELETE FROM recipes WHERE id = ?");
$deleteStmt->execute([$id]);

/*
|--------------------------------------------------------------------------
| REDIRECT
|--------------------------------------------------------------------------
*/

header("Location: ../manage-recipes.php");
exit();
?>